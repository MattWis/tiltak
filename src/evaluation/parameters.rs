use crate::position::num_square_symmetries;

pub const NUM_VALUE_FEATURES_4S: usize = 51;
pub const NUM_POLICY_FEATURES_4S: usize = 98;

pub const NUM_VALUE_FEATURES_5S: usize = 69;
